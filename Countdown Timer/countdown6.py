# ‚è≥ Countdown Timer in Python

import time

def countdown_timer(seconds):
    while seconds:
        mins = seconds // 60
        secs = seconds % 60
        timer_display = f"{mins:02d}:{secs:02d}"
        print(f"‚è∞ Time Left: {timer_display}", end="\r")
        time.sleep(1)
        seconds -= 1

    print("\n‚è≤Ô∏è Time's up! üéâ")

# üîΩ Get time from user
try:
    total_time = int(input("Enter countdown time in seconds: "))
    print("\nStarting countdown...\n")
    countdown_timer(total_time)
except ValueError:
    print("‚ö†Ô∏è Please enter a valid number!")

