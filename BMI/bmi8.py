import streamlit as st

# ğŸ§® BMI Calculation Function
def calculate_bmi(weight, height):
    if height == 0:
        return 0
    return weight / (height ** 2)

# ğŸŒ Streamlit App UI
st.set_page_config(page_title="BMI Calculator", page_icon="ğŸ’ª", layout="centered")

st.title("ğŸ’ª BMI Calculator")
st.write("### Check your Body Mass Index")

# ğŸ“¥ Input Fields
weight = st.number_input("Enter your weight (in kg)", min_value=1.0, step=0.5)
height_cm = st.number_input("Enter your height (in cm)", min_value=10.0, step=0.5)

# ğŸ“ Convert height to meters
height_m = height_cm / 100

# ğŸ§® Calculate BMI on button click
if st.button("Calculate BMI"):
    bmi = calculate_bmi(weight, height_m)
    st.success(f"Your BMI is: **{bmi:.2f}**")

    # ğŸ“Š BMI Categories
    if bmi < 18.5:
        st.warning("You're underweight ğŸ¥º")
    elif 18.5 <= bmi < 24.9:
        st.info("You're healthy ğŸ˜")
    elif 25 <= bmi < 29.9:
        st.warning("You're overweight ğŸ˜…")
    else:
        st.error("You're obese ğŸ¥µ")

