low = 1
high = 100
attempts = 0

while low <= high:
    guess = (low + high) // 2
    attempts += 1

    print(f"My guess is: {guess}")
    feedback = input("Is it too high (h), too low (l), or correct (c)? ").lower()

    if feedback == 'h':
        high = guess - 1
    elif feedback == 'l':
        low = guess + 1
    elif feedback == 'c':
        print(f"\nðŸŽ‰ Yay! I guessed your number in {attempts} attempts.")
        break
    else:
        print("âš ï¸ Invalid input! Please enter only 'h', 'l', or 'c'.")

