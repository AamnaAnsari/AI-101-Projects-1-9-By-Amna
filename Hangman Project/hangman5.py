# ğŸ® Hangman Game in Python

import random

# ğŸ“š Word list (you can add more!)
words = ['python', 'developer', 'hangman', 'challenge', 'program', 'project']
word = random.choice(words).lower()

# ğŸ“Š Game variables
guessed_letters = []
tries = 6
display_word = ['_'] * len(word)

print("ğŸª“ Welcome to the HANGMAN Game!")
print("Guess the letters before the hangman is fully drawn!\n")

# ğŸŒ€ Game loop
while tries > 0 and '_' in display_word:
    print("Word: ", ' '.join(display_word))
    print(f"â¤ï¸ Lives left: {tries}")
    guess = input("Enter a letter: ").lower()

    if len(guess) != 1 or not guess.isalpha():
        print("âš ï¸ Please enter a single valid alphabet letter!\n")
        continue

    if guess in guessed_letters:
        print("ğŸ” You already guessed that letter!\n")
        continue

    guessed_letters.append(guess)

    if guess in word:
        print("âœ… Good guess!\n")
        for i in range(len(word)):
            if word[i] == guess:
                display_word[i] = guess
    else:
        print("âŒ Wrong guess!\n")
        tries -= 1

# ğŸ Game result
if '_' not in display_word:
    print(f"\nğŸ‰ Congratulations! You guessed the word: {word}")
else:
    print(f"\nğŸ’€ Game Over! The word was: {word}")